// Igual a (==) , Diferente de (!=) - Consideram apenas o valor, desconsiderando o tipo.
console.log(5 == '5'); // true, porque os valores são iguais
console.log(5 != '5'); // false, porque os valores são iguais

// Estritamente igual a (===) , Não estritamente igual a (!==)
// Elas consideram tanto o valor quanto o tipo.
console.log(5 === '5'); // false, porque o tipo é diferente
console.log(5 !== '5'); // true, porque o tipo e o valor são diferentes

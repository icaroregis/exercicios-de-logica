let cosmicNumbers = [3, 6, 9, 12];

// Use o loop “for” para exibir cada número duplicado:
for (let i = 0; i < cosmicNumbers.length; i++) {
  console.log(cosmicNumbers[i] * 2); // Doubling each number for extra energy
}

// Use o loop ‘for-of’ para exibir cada número como está:
for (let number of cosmicNumbers) {
  console.log(number); // Displaying the original cosmic power numbers
}
